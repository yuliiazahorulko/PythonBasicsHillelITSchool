# Операції з цілими числами
a = 10
b = 3
print(a + b)  # 13 (додавання)
print(a - b)  # 7  (віднімання)
print(a * b)  # 30 (множення)
print(a // b) # 3  (цілочисельне ділення)
print(a % b)  # 1  (залишок від ділення)


print(a / b)  # 3.3333333333333335   (ділення)
print(f"{a / b:.2f}")  # 3.3333333333333335   (ділення)
print("{:.2f}".format(a / b))  # 3.3333333333333335   (ділення)
print(round(a / b, 2))  # 3.3333333333333335   (ділення)


# Операції з числами з плаваючою комою:
a = 10.5
b = 3.0
print(a + b)  # 13.5  (додавання)
print(a - b)  # 7.5   (віднімання)
print(a * b)  # 31.5  (множення)
print(a / b)  # 3.5   (ділення)
print(a // b) # 3.0   (цілочисельне ділення, результат типу float)
print(a % b)  # 1.5   (залишок від ділення)


# math.sqrt(x) — Квадратний корінь Функція для обчислення квадратного кореня числа.
x = 16
result = math.sqrt(x)
print(result)  # Виведе: 4.0

# math.factorial(x) — Факторіал Обчислює факторіал числа (добуток усіх цілих чисел від 1 до x).
x = 5
result = math.factorial(x)
print(result)  # Виведе: 120 (5! = 5 * 4 * 3 * 2 * 1)

# math.pow(x, y) — Степінь Обчислює число x підняте в степінь y.
x = 2
y = 3
result = math.pow(x, y)
print(result)  # Виведе: 8.0 (2^3)

# math.ceil(x) — Округлення в більшу сторону Округлює число до найближчого більшого або рівного цілого.
x = 3.14
result = math.ceil(x)
print(result)  # Виведе: 4

# math.floor(x) — Округлення в меншу сторону Округлює число до найближчого меншого або рівного цілого.
x = 3.14
result = math.floor(x)
print(result)  # Виведе: 3

# math.sin(x) — Синус Обчислює синус кута x, де x — це кут у радіанах.
x = math.pi / 2  # 90 градусів у радіанах
result = math.sin(x)
print(result)  # Виведе: 1.0

# math.log(x, base) — Логарифм Обчислює логарифм числа x за базою base. Якщо база не вказана,
# обчислюється натуральний логарифм.
x = 100
base = 10
result = math.log(x, base)
print(result)  # Виведе: 2.0 (log_10(100))

# math.exp(x) — Експоненціальна функція Обчислює значення e^x, де e — це математична константа, наближена до 2.71828.
x = 2
result = math.exp(x)
print(result)  # Виведе: 7.3890560989306495 (e^2)

# math.radians(x) — Переведення градусів в радіани Переводить кут x у градусах в радіани.
x = 180
result = math.radians(x)
print(result)  # Виведе: 3.141592653589793 (180 градусів = pi радіан)

# math.degrees(x) — Переведення радіанів в градуси Переводить кут x в радіанах в градуси.
x = math.pi  # pi радіан
result = math.degrees(x)
print(result)  # Виведе: 180.0


