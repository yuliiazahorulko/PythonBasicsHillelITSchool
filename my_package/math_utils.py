def subtract(a, b):
    """Повертає різницю a і b."""
    return a - b


def multiply(a, b):
    """Повертає добуток a і b."""
    return a * b


def power(base, exp):
    """Повертає base, піднесене до степеня exp."""
    return base ** exp


